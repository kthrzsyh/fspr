public class OperatorPenugasan {
    public static void main(String[] args) {
        int a;
        int b;
        a = 5;
        b = 10;

        // penambahan
        b += a;
        System.out.println("penambahan = " + b);

        b -= a;
        System.out.println("pengurangan = " + b);

        b *= a;
        System.out.println("perkalian = " + b);

        b /= a;
        System.out.println("perkalian = " + b);

        b %= a;
        System.out.println("perkalian = " + b);
    }
}
